package egovframework.example.pogo.vo;

public class Pogo5pc2VO {

    private String basId;
    private String div;
    private String name;
    private String pogo5fPow;
    private String pogo5fCat;
    private String pogo5sPow;
    private String pogo5sCat;
    private String pogo5tPow;
    private String pogo5tCat;
    private String pogoMax;
    private int pogoTotal;

    public String getBasId() { return basId; }
    public void setBasId(String basId) { this.basId = basId; }

    public String getDiv() { return div; }
    public void setDiv(String div) { this.div = div; }

    public String getName() { return name; }
    public void setName(String name) { this.name = name; }

    public String getPogo5fPow() { return pogo5fPow; }
    public void setPogo5fPow(String pogo5fPow) { this.pogo5fPow = pogo5fPow; }

    public String getPogo5fCat() { return pogo5fCat; }
    public void setPogo5fCat(String pogo5fCat) { this.pogo5fCat = pogo5fCat; }

    public String getPogo5sPow() { return pogo5sPow; }
    public void setPogo5sPow(String pogo5sPow) { this.pogo5sPow = pogo5sPow; }

    public String getPogo5sCat() { return pogo5sCat; }
    public void setPogo5sCat(String pogo5sCat) { this.pogo5sCat = pogo5sCat; }

    public String getPogo5tPow() { return pogo5tPow; }
    public void setPogo5tPow(String pogo5tPow) { this.pogo5tPow = pogo5tPow; }

    public String getPogo5tCat() { return pogo5tCat; }
    public void setPogo5tCat(String pogo5tCat) { this.pogo5tCat = pogo5tCat; }

    public String getPogoMax() { return pogoMax; }
    public void setPogoMax(String pogoMax) { this.pogoMax = pogoMax; }

    public int getPogoTotal() { return pogoTotal; }
    public void setPogoTotal(int pogoTotal) { this.pogoTotal = pogoTotal; }

}
